const translations: Record<LanguageCode, Dictionary> = {
  ru,
  uk,
  en,
  de,
  fr,
};

const locales: Record<LanguageCode, string> = {
  ru: 'ru-RU',
  uk: 'uk-UA',
  en: 'en-US',
  de: 'de-DE',
  fr: 'fr-FR',
};

const languageNativeLabels: Record<LanguageCode, string> = {
  uk: 'Українська',
  ru: 'Русский',
  en: 'English',
  de: 'Deutsch',
  fr: 'Français',
};

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, key: string) => {
    const value = params[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}

export function t(language: LanguageCode, key: TranslationKey, params?: TranslationParams): string {
  return interpolate(translations[language][key], params);
}

export function getLanguageNativeLabel(language: LanguageCode): string {
  return languageNativeLabels[language];
}

export function formatMonthLabel(monthId: string, language: LanguageCode): string {
  const [yearPart, monthPart] = monthId.split('-');
  const year = Number(yearPart);
  const month = Number(monthPart);

  if (!Number.isFinite(year) || !Number.isFinite(month) || month < 1 || month > 12) {
    return monthId;
  }

  const date = new Date(Date.UTC(year, month - 1, 1));
  return new Intl.DateTimeFormat(locales[language], {
    month: 'long',
    year: 'numeric',
  }).format(date);
}

export function formatMonthName(monthId: string, language: LanguageCode): string {
  const [yearPart, monthPart] = monthId.split('-');
  const year = Number(yearPart);
  const month = Number(monthPart);

  if (!Number.isFinite(year) || !Number.isFinite(month) || month < 1 || month > 12) {
    return monthId;
  }

  const date = new Date(Date.UTC(year, month - 1, 1));
  return new Intl.DateTimeFormat(locales[language], {
    month: 'long',
  }).format(date);
}

